    private void srediFormu() {
        this.setTitle("Pretraga pronalazaka");
        lblErrorDatum.setVisible(false);
        tblPronalazak.setModel(new ModelTabelePronalazak(new ArrayList<Pronalazak>()));
        popuniTabelu();
    }

    private void popuniTabelu() {
        
        List<Pronalazak> listaPronalazaka;
        try {
            Pronalazak pronalazak=new Pronalazak();
            listaPronalazaka = Kontroler.vratiInstancu().nadjiPronalaske(pronalazak);
            ModelTabelePronalazak mtp=new ModelTabelePronalazak(listaPronalazaka);
            tblPronalazak.setModel(mtp);
        } catch (Exception ex) {
            Logger.getLogger(FrmPretragaPronalazaka.class.getName()).log(Level.SEVERE, null, ex);
        }
       
    }
    public void pretraga(){
        Pronalazak pronalazak=new Pronalazak();
        ModelTabelePronalazak mtp=(ModelTabelePronalazak) tblPronalazak.getModel();
        Date datumResavanjaSlucaja;
        try {
            datumResavanjaSlucaja=Kontroler.vratiInstancu().parseDatum(txtDatumResavanjaSlucaja.getText().trim());
        } catch (ParseException ex) {
            //JOptionPane.showMessageDialog(this, "Format datuma koji je prihvatljiv je dd-MM-yyyy");
            lblErrorDatum.setVisible(true);
            lblErrorDatum.setText("Format datuma koji je prihvatljiv je dd-MM-yyyy");
            //Logger.getLogger(FrmPretragaPronalazaka.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Sistem ne može da nađe pronalaske po zadatoj vrednosti", "Greška", JOptionPane.ERROR_MESSAGE);
            return;
        }
        lblErrorDatum.setVisible(false);
        pronalazak.setDatumResavanjaSlucaja(datumResavanjaSlucaja);
        List<Pronalazak> listaPronalaska;
        try {
            listaPronalaska = Kontroler.vratiInstancu().nadjiPronalaske(pronalazak);
            mtp.osvezi(listaPronalaska);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Greška", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(FrmPretragaPronalazaka.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

    void osveziListu() {
        ModelTabelePronalazak mtp=(ModelTabelePronalazak) tblPronalazak.getModel();
        try {
            mtp.osvezi(Kontroler.vratiInstancu().nadjiPronalaske(new Pronalazak()));
        } catch (Exception ex) {
            Logger.getLogger(FrmPretragaPronalazaka.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
