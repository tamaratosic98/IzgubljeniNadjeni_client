    private void btnDetaljiPronalaskaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetaljiPronalaskaActionPerformed
        // TODO add your handling code here:
        int index=tblPronalazak.getSelectedRow();
        if(index>=0){ 
            ModelTabelePronalazak mtp=(ModelTabelePronalazak) tblPronalazak.getModel();
            Pronalazak pronalazak;
            try {
                pronalazak = Kontroler.vratiInstancu().ucitajPronalazak(mtp.getLista().get(index));
                new FrmPronalazak(this, true, EnumModForme.SHOW, pronalazak, this).setVisible(true);
            } catch (Exception ex) {
                Logger.getLogger(FrmPretragaPronalazaka.class.getName()).log(Level.SEVERE, null, ex);
            }
           
        }else{
            JOptionPane.showMessageDialog(this, "Sistem ne može da učita pronalazak. ", "Greška", JOptionPane.ERROR_MESSAGE);
        }
        
        
    }//GEN-LAST:event_btnDetaljiPronalaskaActionPerformed

